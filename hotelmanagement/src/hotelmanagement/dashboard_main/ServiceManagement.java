    public void loadRoomData() {
        DefaultTableModel model = (DefaultTableModel) tblServices.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ trong bảng

    try {
        dba_connection connect = new dba_connection();
        Class.forName(connect.driver);
        Connection con = DriverManager.getConnection(connect.url, connect.username, connect.password);
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT * FROM DVTIENICH");

        while (rs.next()) {
            String maPhong = rs.getString("MADVTI");
            String loaiPhong = rs.getString("TENDVTI");
            String moTa = rs.getString("MOTA");
            String donGia = rs.getString("DONGIA");
            String tinhTrang = rs.getString("TINHTRANG");

            model.addRow(new Object[]{maPhong, loaiPhong, moTa, donGia, tinhTrang});
        }

        rs.close();
        stmt.close();
        con.close();
    } catch (SQLException | ClassNotFoundException e) {
       JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
}
    
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblServices.getSelectedRow(); // lấy dòng được chọn

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please choose a service to delete!", "Notification", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Xác nhận xóa
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this service?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        // Xoá dòng khỏi bảng
        DefaultTableModel model = (DefaultTableModel) tblServices.getModel();
        model.removeRow(selectedRow);
        
    }//GEN-LAST:event_btnDeleteActionPerformed
    
