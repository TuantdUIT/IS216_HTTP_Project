    public void loadRoomData() {
        DefaultTableModel model = (DefaultTableModel) tblRooms.getModel();
        model.setRowCount(0); // Xóa dữ liệu cũ trong bảng

    try {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        String url = "jdbc:oracle:thin:@localhost:1521:orcltdt"; // Thay đổi theo cấu hình của bạn
        String user = "java01";
        String password = "java01";
        Connection con = DriverManager.getConnection(url, user, password);
        Statement stmt = con.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT * FROM DVPHONG");

        while (rs.next()) {
            String maPhong = rs.getString("MADVP");
            String loaiPhong = rs.getString("LOAIPHONG");
            String moTa = rs.getString("MOTA");
            String donGia = rs.getString("DONGIA");
            String tinhTrang = rs.getString("TINHTRANG");

            model.addRow(new Object[]{maPhong, loaiPhong, moTa, donGia, tinhTrang});
        }

        rs.close();
        stmt.close();
        con.close();
    } catch (SQLException | ClassNotFoundException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
}
    
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
//        DeleteRoomForm deleteRoomForm = new DeleteRoomForm();
//        deleteRoomForm.setVisible(true);
        int selectedRow = tblRooms.getSelectedRow(); // lấy dòng được chọn

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xóa!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Xác nhận xóa
        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa phòng này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        // Xoá dòng khỏi bảng
        DefaultTableModel model = (DefaultTableModel) tblRooms.getModel();
        model.removeRow(selectedRow);
        
    }//GEN-LAST:event_btnDeleteActionPerformed
    
